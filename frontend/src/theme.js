// ==================== 적용 함수 ====================

export const applyTheme = (themeKey) => {
  const t = themes[themeKey] || themes.tanzanite
  const root = document.documentElement
  root.style.setProperty('--bg',           t.bg)
  root.style.setProperty('--surface',      t.surface)
  root.style.setProperty('--surface-alt',  t.surfaceAlt)
  root.style.setProperty('--border',       t.border)
  root.style.setProperty('--text',         t.text)
  root.style.setProperty('--text-sub',     t.textSub)
  root.style.setProperty('--text-muted',   t.textMuted)
  root.style.setProperty('--accent',       t.accent)
  root.style.setProperty('--accent-light', t.accentLight)
  root.style.setProperty('--accent-text',  t.accentText)
  root.style.setProperty('--green',        t.green)
  root.style.setProperty('--green-light',  t.greenLight)
  root.style.setProperty('--red',          t.red)
  root.style.setProperty('--red-light',    t.redLight)
  root.style.setProperty('--yellow',       t.yellow)
  root.style.setProperty('--shadow',       t.shadow)
  localStorage.setItem('theme', themeKey)
}

export const applyFontFamily = (fontKey) => {
  const font = fontFamilies.find(f => f.key === fontKey) || fontFamilies[0]
  // 구글 폰트 동적 로드
  if (font.url && !document.querySelector(`link[data-font="${fontKey}"]`)) {
    const link = document.createElement('link')
    link.rel = 'stylesheet'
    link.href = font.url
    link.setAttribute('data-font', fontKey)
    document.head.appendChild(link)
  }
  document.documentElement.style.setProperty('--font-family', font.value)
  localStorage.setItem('font_family', fontKey)
}

export const applyFontSize = (sizeKey) => {
  const size = fontSizes.find(s => s.key === sizeKey) || fontSizes[2]
  document.documentElement.style.setProperty('--font-size',    size.base)
  document.documentElement.style.setProperty('--font-size-sm', size.sm)
  document.documentElement.style.setProperty('--font-size-lg', size.lg)
  localStorage.setItem('font_size', sizeKey)
}

// 앱 시작 시 저장된 설정 복원
export const initTheme = () => {
  applyTheme(localStorage.getItem('theme') || 'tanzanite')
  applyFontFamily(localStorage.getItem('font_family') || 'pretendard')
  applyFontSize(localStorage.getItem('font_size') || 'md')
}